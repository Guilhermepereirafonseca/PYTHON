"""###**WHILE** - *ETEC*"""

# Laço 'while' para imprimir os números de 1 a 5
# faz em sequencia, adicionando +=
# não é uma matriz, mas sim uma sequencia normal.
# \n = pula a linha.

print("\nUsando o laço 'while':")
contador = 1
while contador <= 100:
    print(contador)
    contador += 1

"""###**FOR e RANGE** - *ETEC*"""

# outra forma de laço
# range se baseia na reta númerica.
# x = número elevado (**) a 2

print("\nUsando a compreensão de lista:")
quadrados = [x ** 2 for x in range(1, 6)]
print(quadrados)

