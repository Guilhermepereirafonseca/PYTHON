# Biblioteca que gera números aleatórios.
# o 'f' em 'print' e a função 'format()'
# só vai sair do laço de repetição se você acertar.
import random

# Gerar um número aleatório entre 1 e 100.
numero_aleatorio = random.randint(1, 100)

# Iniciar o loop de adivinhação:
adivinhou = False # Booleano
tentativas = 0

print("Bem-vindo ao jogo de Adivinhação!!")
print("Tente adivinhar o número entre 1 e 100.")

while not adivinhou:
    # Pedir ao usuário para adivinhar
    tentativa = int(input("Digite o seu palpite:"))

    # Verificar se o palpite está correto
    if tentativa == numero_aleatorio:
        tentativas += 1
        print(f"Parabéns! você acertou o número em {tentativas} tentativas.")
        adivinhou = True # Booleano
    elif tentativa < numero_aleatorio:
        print("O número é maior, tente novamente.")
        tentativas += 1
    else:
        print("O número é menor, tente novamente.")
        tentativas += 1